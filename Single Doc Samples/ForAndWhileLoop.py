# Initialize a variable for the while loop
count = 0

# While loop that runs 5 times
while count < 5:
    print(f"While loop iteration: {count + 1}")
    count += 1

# For loop that iterates over a range of numbers
print("\nFor loop output:")
for i in range(5):
    print(f"For loop iteration: {i + 1}")

# Adding some numbers
num1 = 10
num2 = 20
sum = num1 + num2
print(f"\nThe sum of {num1} and {num2} is {sum}")

# Printing a message
print("\nHello, Python!")
